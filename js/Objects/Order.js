function Order(firstName, lastName, address, phone, comment, cfId, cfName, cfSize, cfTopping, cfQuantity, cfTotal, cfPayment) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.address = address;
    this.phone = phone;
    this.comment = comment;
    this.cfId = cfId;
    this.cfName = cfName;
    this.cfSize = cfSize;
    this.cfTopping = cfTopping;
    this.cfQuantity = cfQuantity;
    this.cfTotal = cfTotal;
    this.cfPayment = cfPayment;
}